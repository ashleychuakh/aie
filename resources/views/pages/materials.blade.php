<script>
"use strict"
$(function() {
  $('.owl-carousel').owlCarousel({
      items: 1,
      margin: 10,
      loop: true,
      dots: true,
      autoplay: false,
      slideBy: 3
  });

  function increaseBarsPlz(count) {
    $('.tobeincreased-' + count).animate({
      width: "70%"
    }, 400, "swing");

    $('.tobeincreasedsecondary-' + count).animate({
      width: "60%"
    }, 400, "swing");
  }

  var options = [
    {selector: '.materials-items-container', offset: 650, callback: function() {
      increaseBarsPlz(1);
    } },
    {selector: '.materials-items-container', offset: 1650, callback: function() {
      increaseBarsPlz(2);
    } },
    {selector: '.materials-items-container', offset: 2150, callback: function() {
      increaseBarsPlz(3);
    } },
    {selector: '.materials-items-container', offset: 3150, callback: function() {
      increaseBarsPlz(4);
    } },
    {selector: '.materials-items-container', offset: 4050, callback: function() {
      increaseBarsPlz(5);
    } }
  ];
  Materialize.scrollFire(options);

  $(document).on('click', 'a[href^="#material-item"]', function(e) {
      // target element id
      var id = $(this).attr('href');

      // target element
      var $id = $(id);
      if ($id.length === 0) {
          return;
      }

      // prevent standard hash navigation (avoid blinking in IE)
      e.preventDefault();

      // top position relative to the document
      var pos = $(id).offset().top;

      // animated top scrolling
      $('body, html').animate({scrollTop: pos}, 800, "swing");
  });
});
</script>
